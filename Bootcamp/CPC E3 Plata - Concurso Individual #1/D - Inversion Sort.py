# Fast power - a^b % mod
def powmod(a, b, mod=MOD):
    res = 1
    a = a % mod
    while b > 0:
        if b % 2:
            res = mul(res, a, mod)
        a = mul(a, a, mod)
        b //= 2
    return res


# Inverso multiplicativo de a modulo m (cuando m es primo)
def invmod(a, mod=MOD): return powmod(a, mod - 2, mod)


# GCD y LCM
def lcm(a, b): return a * b // gcd(a, b)


# Factorial con memoización
@lru_cache(maxsize=None)
def factorial(n): return n * factorial(n - 1) if n else 1


# Combinaciones con memoización (nCr)
@lru_cache(maxsize=None)
def comb(n, r):
    if r == 0 or r == n: return 1
    return comb(n - 1, r - 1) + comb(n - 1, r)


def minimum_inversions(current, desired):
    if current == desired:
        return 0
    def invert_subsequence(s, start, end):
        return s[:start] + s[start:end + 1][::-1] + s[end + 1:]
    queue = deque([(current, 0)])
    visited = {current}

    while queue:
        state, operations = queue.popleft()
        if state == desired:
            return operations

        for start in range(len(state)):
            for end in range(start + 1, len(state)):
                new_state = invert_subsequence(state, start, end)
                if new_state not in visited:
                    visited.add(new_state)
                    queue.append((new_state, operations + 1))

    return -1

