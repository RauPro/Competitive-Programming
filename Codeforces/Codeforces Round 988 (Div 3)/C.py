def solve(n):
    if n < 5:
        return [-1]
    ans = []
    for i in range(1, n + 1, 2):
        ans.append(i)
    curr = n if n % 2 else n - 1
    new_curr = -1
    for i in range(2, n + 1, 2):
        if not isPrime(curr + i):
            ans.append(i)
            new_curr = i
            break
    ans += [i for i in range(2, n + 1, 2) if i != new_curr]
    return [-1] if new_curr == -1 else ans


